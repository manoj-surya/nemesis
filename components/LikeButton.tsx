"use client";

import { useEffect, useState } from "react";
import { AiOutlineHeart, AiFillHeart } from "react-icons/ai";
import { useRouter } from "next/navigation";
import { toast } from "react-hot-toast";
import { useSessionContext } from "@supabase/auth-helpers-react";
import { useUser } from "@/hooks/useUser";
import useAuthModal from "@/hooks/useAuthModal";

interface LikeButtonProps {
    songId: string;
}

const LikeButton: React.FC<LikeButtonProps> = ({ songId }) => {
    const router = useRouter();
    const { supabaseClient } = useSessionContext();
    const authModal = useAuthModal();
    const { user } = useUser();

    const [isLiked, setIsLiked] = useState<boolean>(false);

    useEffect(() => {
        if (!user?.id) {
            return;
        }

        /**
         * Checks whether the song is liked by the user.
         */
        const fetchData = async () => {
            const { data, error } = await supabaseClient
                .from("liked_songs")
                .select("*")
                .eq("user_id", user.id)
                .eq("song_id", songId)
                .single();

            if (!error && data) {
                setIsLiked(true);
            }
        };

        fetchData();
    }, [songId, supabaseClient, user?.id]);

    const Icon = isLiked ? AiFillHeart : AiOutlineHeart; // change icon depending on whether the song is liked or not

    /**
     * Allows the user to like or unlike a song.
     *
     * @returns (() => void): open the authentication modal if the user is not logged in
     */
    const handleLike = async () => {
        if (!user) {
            // open the authentication modal if the user is not logged in
            return authModal.onOpen();
        }

        if (isLiked) {
            // unlike the song if it is already liked
            const { error } = await supabaseClient
                .from("liked_songs")
                .delete()
                .eq("user_id", user.id)
                .eq("song_id", songId);

            if (error) {
                toast.error(error.message);
            } else {
                setIsLiked(false);
            }
        } else {
            // like the song if it is not liked
            const { error } = await supabaseClient.from("liked_songs").insert({
                song_id: songId,
                user_id: user.id,
            });

            if (error) {
                toast.error(error.message);
            } else {
                setIsLiked(true);
            }
        }

        router.refresh();
    };

    return (
        <button
            className="
        cursor-pointer 
        hover:opacity-75 
        transition
      "
            onClick={handleLike}
        >
            <Icon color={isLiked ? "#ff0000" : "white"} size={25} />
        </button>
    );
};

export default LikeButton;